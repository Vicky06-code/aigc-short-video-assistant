function interpolate(text, params = {}) {
  return Object.entries(params).reduce((value, [key, param]) => value.replaceAll(`{${key}}`, param), text);
}

export function setLocale(value) {
  if (!messages[value]) return;
  locale.value = value;
  localStorage.setItem('locale', value);
}

export function useI18n() {
  const currentLocale = computed(() => locale.value);
  const currentLocaleLabel = computed(() => locales.find((item) => item.value === locale.value)?.label || locales[0].label);

  function t(key, params) {
    return interpolate(messages[locale.value]?.[key] || messages['zh-CN'][key] || key, params);
  }

  return {
    locale: currentLocale,
    localeOptions: locales,
    currentLocaleLabel,
    setLocale,
    t
  };
}
